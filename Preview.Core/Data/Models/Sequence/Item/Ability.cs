public static partial class SequenceExtensions
{
	public static string GetText(this MainAbilitySeq ability, long value = 0) => ability == default ? null : GetAbilityText(ability, value);

	public static string GetText(this AttachAbilitySeq ability, long value = 0) => ability == default ? null : GetAbilityText(ability, value);

	private static string GetAbilityText(Enum ability, long value)
	{
		var name = SequenceExtensions.GetText(ability);
		if (value == 0) return name;

		return ability.ToString().EndsWith("percent", StringComparison.OrdinalIgnoreCase) ?
			string.Format("{0} {1:0.0}%", name, value * 0.1d) :
			string.Format("{0} {1}", name, value);
	}
}